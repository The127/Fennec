    public static IReadOnlyList<EmojiEntry> Search(string query)
    {
        if (string.IsNullOrWhiteSpace(query))
            return All;

        return All.Where(e => e.Shortcode.Contains(query, StringComparison.OrdinalIgnoreCase)).ToList();
